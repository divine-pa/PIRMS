    const allNotificationsEl = document.getElementById('allNotifications');
    const unreadNotificationsEl = document.getElementById('unreadNotifications');
    const readNotificationsEl = document.getElementById('readNotifications');
    const unreadCountEl = document.getElementById('unreadCount');
    const markAllReadBtn = document.getElementById('markAllReadBtn');
    const clearReadBtn = document.getElementById('clearReadBtn');

    function renderNotifications() {
      allNotificationsEl.innerHTML = '';
      unreadNotificationsEl.innerHTML = '';
      readNotificationsEl.innerHTML = '';

      let unreadCount = 0;
      let readCount = 0;

      notifications.forEach((n) => {
        const notifEl = document.createElement('div');
        notifEl.className = 'list-group-item d-flex align-items-start gap-3 rounded-3 border ' + (n.read ? '' : 'notification-unread');

        notifEl.innerHTML = `
          <div class="notification-icon-container ${n.bgColor} ${n.iconColor}">
            <i class="bi ${n.iconClass} fs-4"></i>
          </div>
          <div class="flex-grow-1">
            <div class="d-flex justify-content-between align-items-start">
              <h6 class="mb-1">${n.title}</h6>
              ${!n.read ? '<span class="badge bg-info align-self-start">New</span>' : ''}
            </div>
            <p class="mb-1 text-muted">${n.message}</p>
            <small class="text-muted">${n.timestamp}</small>
          </div>
          <div class="d-flex flex-column gap-2 ms-3">
            ${!n.read ? `<button type="button" class="btn btn-sm btn-outline-success" title="Mark as read" onclick="markAsRead('${n.id}')"><i class="bi bi-check-lg"></i></button>` : ''}
            <button type="button" class="btn btn-sm btn-outline-danger" title="Delete" onclick="deleteNotification('${n.id}')"><i class="bi bi-trash"></i></button>
          </div>
        `;

        allNotificationsEl.appendChild(notifEl);

        if (!n.read) {
          const unreadEl = notifEl.cloneNode(true);
          unreadNotificationsEl.appendChild(unreadEl);
          unreadCount++;
        } else {
          const readEl = notifEl.cloneNode(true);
          readNotificationsEl.appendChild(readEl);
          readCount++;
        }
      });

      unreadCountEl.textContent = `${unreadCount} Unread`;
      markAllReadBtn.disabled = unreadCount === 0;
      clearReadBtn.disabled = readCount === 0;

      if (notifications.length === 0) {
        allNotificationsEl.innerHTML = `<p class="text-center text-muted py-5">No notifications</p>`;
        unreadNotificationsEl.innerHTML = `<p class="text-center text-muted py-5">No unread notifications</p>`;
        readNotificationsEl.innerHTML = `<p class="text-center text-muted py-5">No read notifications</p>`;
      }
    }

    function markAsRead(id) {
      const notif = notifications.find(n => n.id === id);
      if (notif) {
        notif.read = true;
        renderNotifications();
      }
    }

    function markAllAsRead() {
      notifications.forEach(n => n.read = true);
      renderNotifications();
    }

    function deleteNotification(id) {
      const index = notifications.findIndex(n => n.id === id);
      if (index !== -1) {
        notifications.splice(index, 1);
        renderNotifications();
      }
    }

    function clearRead() {
      for(let i = notifications.length - 1; i >= 0; i--) {
        if (notifications[i].read) notifications.splice(i, 1);
      }
      renderNotifications();
    }

    markAllReadBtn.addEventListener('click', () => markAllAsRead());
    clearReadBtn.addEventListener('click', () => clearRead());

    renderNotifications();
  </script>

  
